package ar.edu.unlam.tallerweb1.modelo;

public class ExperienciaForm {
	private Integer idRegalo;
	private Integer idExp;
	private String cupon;
	
	public Integer getIdRegalo() {
		return idRegalo;
	}
	public void setIdRegalo(Integer idRegalo) {
		this.idRegalo = idRegalo;
	}
	public Integer getIdExp() {
		return idExp;
	}
	public void setIdExp(Integer idExp) {
		this.idExp = idExp;
	}
	public String getCupon() {
		return cupon;
	}
	public void setCupon(String cupon) {
		this.cupon = cupon;
	}
	
	
}
